using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Drill12
{
    public class MathOperation
    {
        public int PlusTwo(int num1)
        {
            int result = num1 + 2;
            return result;
        }
        public int TimesTwo(int num2)
        {
            int result = num2 * 2;
            return result;
        }
        public int DividedByTwo(int num3)
        {
            int result = num3 / 2;
            return result;
        }
    }
}
